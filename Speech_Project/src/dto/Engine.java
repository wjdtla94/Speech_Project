package dto;

public class Engine {
	private String email;
	private String engine;
	private String url;
	public String getEmail() {
		return email;
	}
	public Engine setEmail(String email) {
		this.email = email;
		return this;
	}
	public String getEngine() {
		return engine;
	}
	public Engine setEngine(String engine) {
		this.engine = engine;
		return this;
	}
	public String getUrl() {
		return url;
	}
	public Engine setUrl(String url) {
		this.url = url;
		return this;
	}
	
	
}
